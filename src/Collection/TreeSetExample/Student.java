package Collection.TreeSetExample;

public class Student {
    String name;
    int note;
    public Student(String name, int note){
        this.name = name;
        this.note = note;
    }
    public String getName(){
        return this.name;
    }
    public void setName(String name){
        this.name = name;
    }
    public int getNote(){
        return this.note;
    }
    public void setNote(int note){
        this.note = note;
    }


}
